<h2>Welcome to Social Igniter!</h2>

<p>A really simple Social Web Application template built on the CodeIgniter framework that easily integrates with popular social media websites.</p>

<h2>Who Should Use This</h2>
<p>People who program in PHP and know or want to learn the <a href="http://codeigniter.com" target="_blank">CodeIgniter Framework</a> and are looking to setup a new social networking site.</p>

<h2>Features</h2>
<ul>
	<li>User Registration, Login & Authentication</li>
	<li>Email Verification, Forgot Password</li>
	<li>Basic User Creation, Editing, Deleting</li>
	<li>Basic Status Updates</li>
	<li>Basic REST API</li>
	<li>Out of the box social integration (Facebook, Twitter, etc...)</li>
</ul>

<h2>Requirements</h2>
<ul>
	<li>Web Hosting</li>
	<li>PHP 5.0</li>
	<li>MySQL</li>
	<li>10 MB disk space</li>
</ul>

<h2>Under The Hood</h2>

<p>This application is built with the <a href="http://codeigniter.com" target="_blank">CodeIgniter Framework</a> and adheres to <a href="http://en.wikipedia.org/wiki/Model%E2%80%93view%E2%80%93controller" target="_blank">Model View Controller</a> (MVC) design pattern and <a href="http://en.wikipedia.org/wiki/Object-oriented_programming" target="_blank">Object Oriented Programing</a> (OOP). If you are unfamiliar with these methods you may find this application very challenging to extend or help develop for.</p>
<p>If you're willing to learn, hopefully this application <em>will</em> be a good starting point to get into MVC and OOP web applications.</p>
		
